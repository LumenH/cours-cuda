bool useMontecarlo(){
    int nbFlechette = 1000000;
    float result;
    int m = 10;

    int mp = 256;

    dim3 dg = dim3(mp, 1, 1);
    dim3 db = dim3(1024, 1, 1);
    Grid grid(dg, db);

    Montecarlo montecarlo(grid, nbFlechette);
    montecarlo.run();

    result = montecarlo.getResult(m);
    cout<<result<<endl;

    return true;
}

