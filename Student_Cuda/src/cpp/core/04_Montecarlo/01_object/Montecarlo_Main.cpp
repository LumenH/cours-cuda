bool useMontecarlo(){
    int nbFlechette = INT_MAX/10;
    float result;

    int mp = 64;

    dim3 dg = dim3(mp, 1, 1);
    dim3 db = dim3(1024, 1, 1);
    Grid grid(dg, db);

    Montecarlo montecarlo(grid, nbFlechette);
    montecarlo.run();

    result = montecarlo.getResult();
    cout<<result<<endl;

    return true;
}

