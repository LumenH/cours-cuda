bool useSlice(){
    int nbSlice = 9;

    float* ptrResult = new float[1];

    {
	int mp = Device::getMPCount();
	int coreMP = 512;//nbThread par block

	dim3 dg = dim3(mp,1,1);
	dim3 db = dim3(coreMP,1,1);
	Grid grid(dg, db);

	Slice slice(grid, ptrResult, nbSlice);
	slice.run();

    }

    float result = *ptrResult;
    cout<<result<<endl;

    return true;
}

